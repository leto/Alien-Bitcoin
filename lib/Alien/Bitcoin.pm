package Alien::Bitcoin;
use strict;
use warnings;
use base qw( Alien::Base );
our $VERSION = '0.01';

=head1 NAME

Alien::Bitcoin - Easily install Bitcoin full node

=head1 DESCRIPTION

This allows you to tell your favorite CPAN installer to install Bitcoin, via
your local CPAN mirror. This allows Perl applications to specify that they
depend on an external dependency, Bitcoin, in a standard way, the Alien::*
namespace. Most likely you will not use this module directly, you just want
to do

    cpan Alien::Bitcoin

to install with the normal CPAN.pm client or:

    cpanm Alien::Bitcoin

to use the newer, more featureful cpanminus client.

